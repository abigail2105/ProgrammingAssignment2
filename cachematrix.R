makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinv <- function(solve) m <<- solve
  getinv <- function() m
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
}

## This function reads an existing matrix - x MUST be a matrix, then 
## creates and caches the inverse of the matrix 

cacheSolve <- function(x, ...) {
  m <- x$getinv()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinv(m)
  m
}

  ## Returns a matrix that is the inverse of 'x'
  ## if x has ben cached it returns the cached inverse
  ## if there is no cache it calculates and caches the inverse     
}
